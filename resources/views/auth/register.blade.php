<script>
    function togglePassword(fieldId) {
        const passwordField = document.getElementById(fieldId);
        const togglePasswordButton = document.getElementById('toggle-' + fieldId);
        const icon = togglePasswordButton.querySelector('i');

        if (passwordField.type === 'password') {
            passwordField.type = 'text';
            icon.classList.remove('fa-eye');
            icon.classList.add('fa-eye-slash');
        } else {
            passwordField.type = 'password';
            icon.classList.remove('fa-eye-slash');
            icon.classList.add('fa-eye');
        }
    }

    function checkPasswordStrength() {
        const password = document.getElementById('password').value;
        const strengthText = document.getElementById('password-strength');
        let strength = 0;

        if (password.length >= 8) strength += 1;
        if (/[a-z]/.test(password)) strength += 1;
        if (/[A-Z]/.test(password)) strength += 1;
        if (/[0-9]/.test(password)) strength += 1;
        if (/[@$!%*?&]/.test(password)) strength += 1;

        switch (strength) {
            case 0:
            case 1:
                strengthText.innerHTML = 'Password Strength: Weak';
                strengthText.style.color = 'red';
                break;
            case 2:
                strengthText.innerHTML = 'Password Strength: Medium';
                strengthText.style.color = 'orange';
                break;
            case 3:
                strengthText.innerHTML = 'Password Strength: Strong';
                strengthText.style.color = 'blue';
                break;
            case 4:
            case 5:
                strengthText.innerHTML = 'Password Strength: Very Strong';
                strengthText.style.color = 'green';
                break;
        }
    }
</script>
